public class IndexesWithStrings {
    public static void main(String[] args){
      int  studentAge = 17;
      double GPA = 8.3;
      boolean hasPerfectAttendence = true;

      String studentFirstName = "Diego";
      String studentLastName = "Escobedo";
      char studentFirstLetter = studentFirstName.charAt(0);
      char studentLastLetter = studentLastName.charAt(0);


      System.out.println(studentAge);
      System.out.println(GPA);
      System.out.println(studentFirstLetter);
      System.out.println(studentLastLetter);
      System.out.println(hasPerfectAttendence);
      System.out.println(studentFirstName);
      System.out.println(studentLastName);
    }
}

