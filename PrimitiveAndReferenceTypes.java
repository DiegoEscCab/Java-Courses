public class PrimitiveAndReferenceTypes {
    public static void main(String[] args){
      int  studentAge = 17;
      double GPA = 8.3;
      char studentFirstLetter = 'D';
      char studentLastLetter = 'E';
      boolean hasPerfectAttendence = true;
      String studentName = "Diego Escobedo";

      System.out.println(studentAge);
      System.out.println(GPA);
      System.out.println(studentFirstLetter);
      System.out.println(studentLastLetter);
      System.out.println(hasPerfectAttendence);
      System.out.println(studentName);
    }
}

